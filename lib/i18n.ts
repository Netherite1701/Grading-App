type AppCopy = typeof appCopy.en;
export type CopyKey = keyof AppCopy;
export type EditableCopyKey = {
  [Key in CopyKey]: AppCopy[Key] extends string ? Key : never;
}[CopyKey];
export type TranslationOverrides = Partial<Record<AppLanguage, Partial<Record<EditableCopyKey, string>>>>;

export const editableCopyKeys = (Object.keys(appCopy.en) as CopyKey[]).filter((key): key is EditableCopyKey => typeof appCopy.en[key] === "string");

export function getBaseAppCopy(language: AppLanguage) {
  return appCopy[language];
}

export function getAppCopy(language: AppLanguage, overrides?: Partial<Record<EditableCopyKey, string>>) {
  return {
    ...appCopy[language],
    ...overrides
  };
}

export function getRoleLabel(role: Role, language: AppLanguage) {
  const copy = getAppCopy(language);
  if (role === "developer") return copy.developer;
  if (role === "organizer") return copy.organizer;
  if (role === "judge") return copy.judge;
  return copy.guest;
}
